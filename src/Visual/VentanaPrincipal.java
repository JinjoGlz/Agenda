    public void actualizar(){
        for (int i = modelo.getRowCount()-1; i >=0; i--) {
            modelo.removeRow(i);
            
        }
        Nodo<Contacto> aux= contactos.getInicio();
       if(aux==null){
           
       }else{
           if(aux.getLiga()==null){
               Object datos[]={aux.getInfo().getNombre()};
            System.out.println(datos[0]);
            modelo.insertRow(modelo.getRowCount(), datos);
           }else{
        while(aux!=null){
            Object datos[]={aux.getInfo().getNombre()};
            System.out.println(datos[0]);
            modelo.insertRow(modelo.getRowCount(), datos);
            aux=aux.getLiga();
        }
           }
       }
    }
    
    
