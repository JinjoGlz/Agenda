    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
      try{
        int index=jTableTabla.getSelectedRow();
          if(index==0){
              contactos.borrarInicio();
          }else{
              if(index==modelo.getRowCount()-1){
                  contactos.borrarFinal();
              }else{
                  contactos.borrar(index);
              }
          }      
                actualizar();
                }
                catch(ArrayIndexOutOfBoundsException ex){
                    JOptionPane.showMessageDialog(null, "Seleccione un correo para borrar");
                }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        try{
        int index=jTableTabla.getSelectedRow();
        Nodo<Contacto> aux= contactos.getInicio();
        int i=0;
            while(i<index) {
                aux=aux.getLiga();
                i++;
            }
                Informacion in=new Informacion(this, true, aux);
                in.setVisible(true);
                actualizar();
                }
                catch(ArrayIndexOutOfBoundsException ex){
                    JOptionPane.showMessageDialog(null, "Seleccione un Contacto para modificar");
                }
    }//GEN-LAST:event_jButtonModificarActionPerformed

